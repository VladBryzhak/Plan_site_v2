        function pad(n) { return String(n).padStart(2, '0'); }

        function toICSDate(date) {
            return date.getFullYear() + pad(date.getMonth() + 1) + pad(date.getDate()) + 'T' + pad(date.getHours()) + pad(date.getMinutes()) + '00';
        }

        function generateICS() {
            const startVal = document.getElementById('start-date').value;
            if (!startVal) { alert('Вкажи дату початку'); return; }

            const startDate = new Date(startVal + 'T07:00:00');
            const WEEKS = 12;

            let lines = [
                'BEGIN:VCALENDAR',
                'VERSION:2.0',
                'PRODID:-//Fitness Plan//UK',
                'CALSCALE:GREGORIAN',
                'METHOD:PUBLISH',
                'X-WR-CALNAME:Тренування',
                'X-WR-TIMEZONE:Europe/Kiev',
            ];

            const trainDays = [
                { offset: 0, title: 'Тренування: Верхня A (груди, плечі, трицепс)', hour: 7, dur: 50, desc: calDescriptions[0] },
                { offset: 1, title: 'Тренування: Кардіо + кор', hour: 7, dur: 40, desc: calDescriptions[1] },
                { offset: 3, title: 'Тренування: Ноги і сідниці', hour: 7, dur: 50, desc: calDescriptions[3] },
                { offset: 5, title: 'Тренування: Верхня B (спина, біцепс)', hour: 7, dur: 50, desc: calDescriptions[5] },
                { offset: 6, title: 'Тренування: Легкий біг / прогулянка', hour: 7, dur: 35, desc: calDescriptions[6] },
            ];

            for (let w = 0; w < WEEKS; w++) {
                for (const ev of trainDays) {
                    const d = new Date(startDate);
                    d.setDate(d.getDate() + w * 7 + ev.offset);
                    d.setHours(ev.hour, 0, 0, 0);
                    const end = new Date(d.getTime() + ev.dur * 60000);
                    const uid = `fitness-${w}-${ev.offset}-${Date.now()}@fitplan`;
                    lines.push('BEGIN:VEVENT');
                    lines.push('UID:' + uid);
                    lines.push('DTSTART:' + toICSDate(d));
                    lines.push('DTEND:' + toICSDate(end));
                    lines.push('SUMMARY:' + ev.title);
                    lines.push('DESCRIPTION:' + ev.desc.replace(/\n/g, '\\n'));
                    lines.push('END:VEVENT');
                }
            }

            lines.push('END:VCALENDAR');
            const blob = new Blob([lines.join('\r\n')], { type: 'text/calendar;charset=utf-8' });
            const url = URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.href = url;
            a.download = 'trenuvanya.ics';
            a.click();
            URL.revokeObjectURL(url);
            closeCalModal();
            const toast = document.getElementById('toast');
            toast.classList.add('show');
            setTimeout(() => toast.classList.remove('show'), 2500);
        }

